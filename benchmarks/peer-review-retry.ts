async function queryModel(modelId: string, label: string, apiKey: string): Promise<{ label: string; response: string; error?: string }> {
  console.log(`  Querying ${label}...`);
  try {
    const res = await fetch(OPENROUTER_URL, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${apiKey}`,
        'HTTP-Referer': 'https://github.com/chode',
        'X-Title': 'CHODE Peer Review',
      },
      body: JSON.stringify({
        model: modelId,
        messages: [{ role: 'user', content: PROMPT }],
        temperature: 0.3,
        max_tokens: 2500,
      }),
    });

    if (!res.ok) {
      const err = await res.text();
      return { label, response: '', error: `HTTP ${res.status}: ${err}` };
    }

    const data = await res.json() as {
      choices?: Array<{ message?: { content: string } }>;
      error?: { message: string };
    };

    if (data.error) return { label, response: '', error: data.error.message };
    const content = data.choices?.[0]?.message?.content ?? '';
    return { label, response: content };
  } catch (e) {
    return { label, response: '', error: String(e) };
  }
}

async function main() {
  const apiKey = process.env.OPENROUTER_API_KEY;
  if (!apiKey) { console.error('OPENROUTER_API_KEY not set'); process.exit(1); }

  console.log('\nCHODE Peer Review (retry) — querying 3 models\n');
  const results = await Promise.all(MODELS.map(m => queryModel(m.id, m.label, apiKey)));

  // Append to existing file
  const date = new Date().toISOString().slice(0, 10);
  const outFile = resolve(__dirname, 'results', `peer-review-${date}.md`);

  let existing = '';
  try { existing = await readFile(outFile, 'utf8'); } catch {}

  let addendum = `\n---\n\n# Retry Results (corrected model IDs, max_tokens=2500)\n\n`;
  for (const r of results) {
    addendum += `## ${r.label} (retry)\n\n`;
    addendum += r.error ? `**ERROR:** ${r.error}\n\n` : r.response + '\n\n';
    addendum += '---\n\n';
    console.log(`\n${'='.repeat(60)}\n${r.label}\n${'='.repeat(60)}`);
    console.log(r.error ? `ERROR: ${r.error}` : r.response);
  }

  await writeFile(outFile, existing + addendum, 'utf8');
  console.log(`\nAppended to: ${outFile}\n`);
}

main().catch(e => { console.error(e); process.exit(1); });
